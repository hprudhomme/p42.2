int		ft_sqrt(int nb)
{
	int i;

	i = 1;
	if (nb <= 0)
		return (0);
	if (nb >= 2000000)
		i = 1400;
	if (nb >= 1000000)
		i = 999;
	if (nb >= 500000)
		i = 700;
	if (nb >= 100000)
		i = 300;
	while (i * i <= nb)
	{
		if (i * i - nb == 0)
			return (i);
		i++;
	}
	return (0);
}

#include <stdio.h>
int main()
{
	printf("%d\n", ft_sqrt(8));
}
