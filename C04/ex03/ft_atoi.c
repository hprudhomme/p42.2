int		ft_atoi(char *str)
{
	int i;
	int n;
	int res;

	n = 1;
	i = 0;
	while (str[i] == ' ' || str[i] == '\r' || str[i] == '\n' || str[i] == '\t'
			|| str[i] == '\n'
			|| str[i] == '\v' || str[i] == '\f')
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			n = n * -1;
		i++;
	}
	res = 0;
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = (res * 10) + str[i] - 48;
		i++;
	}
	res = res * n;
	return (res);
}
