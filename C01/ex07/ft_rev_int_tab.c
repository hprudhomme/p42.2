void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int j;
	int t;

	i = -1;
	j = size;
	while (++i < --j)
	{
		t = tab[i];
		tab[i] = tab[j];
		tab[j] = t;
	}
}
